import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const configPath = path.join(repoRoot, "gallery-sources.json");
const outputPath = path.join(repoRoot, "gallery-data.js");
const imageExtensions = new Set([".jpg", ".jpeg", ".png", ".webp", ".gif", ".avif"]);

const config = JSON.parse(await readFile(configPath, "utf8"));
const assetRoot = config.assetRoot || "assets";
const assetRootPath = path.join(repoRoot, assetRoot);

async function listImages(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const images = [];

  for (const entry of entries) {
    const absolutePath = path.join(dir, entry.name);

    if (entry.isDirectory()) {
      images.push(...await listImages(absolutePath));
      continue;
    }

    if (!entry.isFile() || !imageExtensions.has(path.extname(entry.name).toLowerCase())) {
      continue;
    }

    const relativePath = path.relative(repoRoot, absolutePath).split(path.sep).join("/");
    images.push(`./${relativePath}`);
  }

  return images;
}

function naturalCompare(a, b) {
  return a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" });
}

function matchesAny(imagePath, matchers) {
  const normalized = imagePath.toLowerCase().replace(/[-_]+/g, " ");
  return matchers.some((matcher) => normalized.includes(matcher.toLowerCase()));
}

const allImages = (await listImages(assetRootPath)).sort(naturalCompare);
const gallery = {};

for (const [categoryKey, category] of Object.entries(config.categories)) {
  gallery[categoryKey] = {
    title: category.title,
    groups: category.groups.map((group) => ({
      name: group.name,
      images: allImages.filter((imagePath) => matchesAny(imagePath, group.match || []))
    }))
  };
}

const generated = `// gallery-data.js
// Generated by scripts/generate-gallery-data.mjs from gallery-sources.json and /${assetRoot}.
// Upload new photos into /${assetRoot} with names that include a configured keyword.
window.GALLERY = ${JSON.stringify(gallery, null, 2)};
`;

await writeFile(outputPath, generated, "utf8");
console.log(`Generated ${path.relative(repoRoot, outputPath)} from ${allImages.length} images.`);
